#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    int a;
    long b;
    char c;
    float d;
    double e;
    scanf("%d", &a);
    printf("%d\n", a);
    scanf("%ld", &b);
    printf("%ld\n", b);
    cin >> c;
    cout << c << endl;
    scanf("%f", &d);
    printf("%f\n", d);
    scanf("%lf", &e);
    printf("%lf", e);

    return 0;
}
